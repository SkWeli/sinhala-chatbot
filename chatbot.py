st.set_page_config(
    page_title="සිංහල AI චැට්බොට්",
    page_icon="🤖",
    layout="centered"
)

st.title("🤖 සිංහල AI චැට්බොට්")
st.caption("🔒 Fully Offline | ⚖️ Constitutional AI | 📚 RAG-Powered | ⚡ Streaming")

# Sidebar: 
with st.sidebar:
    st.header("⚙️ System Status")

    # LLM loaded correctly
    st.success("✅ Qwen2.5:7b loaded")

    # whether RAG knowledge base is available
    if retriever:
        st.success("✅ Knowledge base active")
    else:
        st.warning("⚠️ No knowledge base.\nAdd PDFs to /documents\nthen run: python ingest.py")

    st.divider()
    st.header("⚖️ Active Constitution")
    st.info(CONSTITUTION)

# Session State Initialisation 
# First run: messages list doesn't exist yet - create it empty.
# Subsequent runs: this block is skipped, preserving history.
if "messages" not in st.session_state:
    st.session_state.messages = []

# Clear Chat Button 
if st.button("🗑️ Clear Chat"):
    st.session_state.messages = []
    st.rerun()

# Render Existing Chat History 
# Loop through all stored messages and render them as chat bubbles.
# st.chat_message("user") renders a human bubble,
# st.chat_message("assistant") renders a bot bubble.
for msg in st.session_state.messages:
    with st.chat_message(msg["role"]):
        st.markdown(msg["content"])

# Chat Input + Response Pipeline 
# st.chat_input renders the input box pinned to the bottom.
# The walrus operator (:=) assigns AND checks in one line —
# the block only runs when the user actually submits a message.
if user_input := st.chat_input("සිංහලෙන් ටයිප් කරන්න..."):

    # Store and immediately display the user's message
    st.session_state.messages.append({"role": "user", "content": user_input})
    with st.chat_message("user"):
        st.markdown(user_input)

    # Build all prompt components
    context = get_rag_context(user_input)           # RAG retrieval
    history = build_history(st.session_state.messages[:-1])  # exclude current msg
    full_prompt = build_prompt(user_input, context, history)  # stitch together

    # Stream the response token by token
    with st.chat_message("assistant"):
        response_placeholder = st.empty()
        full_response = ""

        for token in llm.stream(full_prompt):
            full_response += token
            response_placeholder.markdown(full_response + "▌")  # live cursor

        response_placeholder.markdown(full_response)  # final clean render

    # Save the completed response to session history
    st.session_state.messages.append({"role": "assistant", "content": full_response})
