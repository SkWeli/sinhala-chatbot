import os
import streamlit as st
from langchain_ollama import OllamaLLM, OllamaEmbeddings
from langchain_community.vectorstores import Chroma

CONSTITUTION = """
You are a Sinhala language AI assistant. You must strictly follow these rules on every response:

1. LANGUAGE: Always respond in Sinhala Unicode script only. Never mix English words or letters into your Sinhala response. Never use transliterated Sinhala (Singlish). Every single word must be in Sinhala Unicode.
2. SAFETY: Never provide harmful, violent, illegal, or unethical advice under any circumstances.
3. HONESTY: If you don't know the answer, say "මට මේ ගැන තොරතුරු නැත." - never fabricate facts.
4. TONE: Always maintain a respectful, neutral, and polite tone.
5. GROUNDING: Only use the provided context to answer. Do not go beyond it.
6. UNICODE: Always write Sinhala in proper Unicode - never in Singlish or Latin script.
7. VOCABULARY: Only use real, commonly known Sinhala words. If you do not know the correct Sinhala word, use a simpler known word instead of inventing one.
"""

@st.cache_resource
def load_llm():
    """Load the Qwen2.5 7B model via Ollama local server."""
    return OllamaLLM(model="qwen2.5:7b", temperature=0.3)

@st.cache_resource
def load_retriever():
    """
    Load the ChromaDB vector store for RAG retrieval.
    Returns None gracefully if no knowledge base has been ingested yet.
    This allows the chatbot to work even without documents loaded.
    """
    embeddings = OllamaEmbeddings(model="nomic-embed-text")
    if os.path.exists("./chroma_db"):
        vectorstore = Chroma(
            persist_directory="./chroma_db",
            embedding_function=embeddings
        )
        # k=3 means: fetch the 3 most relevant document chunks.
        # More chunks = more context but slower & noisier responses.
        return vectorstore.as_retriever(search_kwargs={"k": 3})
    return None  # No documents ingested yet — chatbot still works

llm = load_llm()
retriever = load_retriever()

def build_prompt(user_input: str, context: str = "", history: str = "") -> str:
    """
    Constructs the full prompt sent to the LLM.
    Order matters: Constitution → Context → History → Question.
    """
    return f"""{CONSTITUTION}

### Knowledge Base Context (use this to answer):
{context if context else "No specific context available. Use general knowledge but stay within rules."}

### Recent Conversation History:
{history if history else "This is the start of the conversation."}

### User's Question:
{user_input}

### Your Sinhala Response:"""


def get_rag_context(query: str) -> str:
    """
    Queries ChromaDB to find document chunks most similar to the user's question.
    Uses cosine similarity on embeddings generated by nomic-embed-text.
    Returns an empty string if no retriever is loaded.
    """
    if retriever:
        docs = retriever.invoke(query)
        # Join the top-k document chunks into a single context block
        return "\n\n".join([doc.page_content for doc in docs])
    return ""


def build_history(messages: list) -> str:
    """
    Converts the last 6 session messages into a readable conversation string.
    This gives the LLM short-term memory within the session.
    """
    recent = messages[-6:]  # Only last 6 messages to stay within context limits
    history = ""
    for msg in recent:
        role = "පරිශීලකයා (User)" if msg["role"] == "user" else "සහායකයා (Assistant)"
        history += f"{role}: {msg['content']}\n"
    return history

